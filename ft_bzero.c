#include "libft.h"

void	*ft_bzero(void *b, size_t n)
{
	size_t	i;

	if (n == 0)
		return (b);
	i = 0;
	while (i < n)
	{
		((char *)b)[i] = '\0';
		i++;
	}
	return (b);
}
