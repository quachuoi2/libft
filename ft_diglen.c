#include "libft.h"

int	ft_diglen(long long n)
{
	int	i;

	i = 1;
	if (n < -9223372036854775807)
		return (20);
	if (n < 0)
	{
		n = -n;
		i = 2;
	}
	while (n / 10 > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
