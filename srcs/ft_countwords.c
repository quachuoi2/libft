#include "libft.h"

int	ft_countwords(const char *s, char c)
{
	int	i;
	int	words;

	i = 0;
	words = 0;
	while (s[i] != '\0')
	{
		if ((s[i] == c) && (s[i + 1] != c) && (s[i + 1] != '\0'))
			words++;
		i++;
	}
	if (s[0] != c)
		words++;
	return (words);
}
