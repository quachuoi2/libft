#include "libft.h"

int	ft_diglen(int n)
{
	int	i;

	i = 1;
	if (n == -2147483648)
		return (10);
	if (n < 0)
		n = -n;
	while (n / 10 > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
